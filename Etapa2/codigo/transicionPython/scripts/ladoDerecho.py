def convertir_formato(texto):
    lineas = texto.strip().splitlines()
    resultado = []

    for linea in lineas:
        linea = linea.strip().replace('{', '[').replace('}', ']')
        resultado.append(f"        {linea}")

    return "\n".join(resultado)

